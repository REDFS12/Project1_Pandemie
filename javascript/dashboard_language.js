// Function to switch language
function switchLanguage(lang) {
    document.querySelector('h1').textContent = translations[lang].welcome;
    document.querySelector('.main-content p').textContent = translations[lang].description;
    document.querySelector('.information h2').textContent = translations[lang].importantInfo;
    document.querySelector('#total_cases').previousSibling.textContent = translations[lang].totalCases;
    document.querySelector('#active_cases').previousSibling.textContent = translations[lang].activeCases;
    document.querySelector('#recovered_cases').previousSibling.textContent = translations[lang].recoveredCases;
    document.querySelector('.info-virus h2').textContent = translations[lang].aboutViruses;

    // Update COVID-19 section
    const covid = translations[lang].covid;
    document.querySelector('.covid-home h3').textContent = covid.title;
    document.querySelector('.covid-home p:nth-of-type(1)').textContent = covid.fullName;
    document.querySelector('.covid-home p:nth-of-type(2)').textContent = covid.causativeAgent;
    document.querySelector('.covid-home p:nth-of-type(3)').textContent = covid.spread;
    document.querySelector('.covid-home p:nth-of-type(4)').textContent = covid.symptoms;
    document.querySelector('.covid-home p:nth-of-type(5)').textContent = covid.prevention;
    document.querySelector('.covid-home p:nth-of-type(6)').textContent = covid.severity;

    // Update Seasonal Flu section
    const flu = translations[lang].flu;
    document.querySelector('.griep-home h3').textContent = flu.title;
    document.querySelector('.griep-home p:nth-of-type(1)').textContent = flu.fullName;
    document.querySelector('.griep-home p:nth-of-type(2)').textContent = flu.causativeAgent;
    document.querySelector('.griep-home p:nth-of-type(3)').textContent = flu.spread;
    document.querySelector('.griep-home p:nth-of-type(4)').textContent = flu.symptoms;
    document.querySelector('.griep-home p:nth-of-type(5)').textContent = flu.prevention;
    document.querySelector('.griep-home p:nth-of-type(6)').textContent = flu.severity;

    // Update Malaria section
    const malaria = translations[lang].malaria;
    document.querySelector('.malaria-home h3').textContent = malaria.title;
    document.querySelector('.malaria-home p:nth-of-type(1)').textContent = malaria.causativeAgent;
    document.querySelector('.malaria-home p:nth-of-type(2)').textContent = malaria.spread;
    document.querySelector('.malaria-home p:nth-of-type(3)').textContent = malaria.symptoms;
    document.querySelector('.malaria-home p:nth-of-type(4)').textContent = malaria.prevention;
    document.querySelector('.malaria-home p:nth-of-type(5)').textContent = malaria.severity;

    // Update details link
    document.querySelector('#details-link').textContent = translations[lang].detailsLink;
}

// Add event listeners to buttons
document.querySelectorAll('.language-buttons button').forEach(button => {
    button.addEventListener('click', () => {
        const lang = button.getAttribute('data-lang');
        localStorage.setItem('selectedLanguage', lang); // Save the selected language
        switchLanguage(lang); // Update the content dynamically
    });
});

// Load the saved language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'en'; // Default to English
    switchLanguage(savedLanguage); // Apply the saved language
});